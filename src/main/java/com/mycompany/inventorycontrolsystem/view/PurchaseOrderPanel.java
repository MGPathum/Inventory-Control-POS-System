    private void addNewItem() {        int productIdx = cmbProduct.getSelectedIndex() - 1; // -1 for placeholder
        if (productIdx < 0) {
            warn("Please select a product."); return;
        }
        com.mycompany.inventorycontrolsystem.model.Product product =
                productList.get(productIdx);        int qty;
        try {
            qty = Integer.parseInt(txtQty.getText().trim());
            if (qty <= 0) throw new NumberFormatException();
        } catch (NumberFormatException ex) {
            warn("Quantity must be a positive whole number.");
            txtQty.requestFocusInWindow();
            return;
        }        java.math.BigDecimal unitCost;
        try {
            unitCost = new java.math.BigDecimal(txtCost.getText().trim());
            if (unitCost.compareTo(java.math.BigDecimal.ZERO) < 0)
                throw new NumberFormatException();
        } catch (NumberFormatException ex) {
            warn("Unit cost must be a valid number (zero or greater).");
            txtCost.requestFocusInWindow();
            return;
        }        for (int row = 0; row < poItemModel.getRowCount(); row++) {
            int existingId = (int) poItemModel.getValueAt(row, 4);
            if (existingId == product.getProductId()) {
                int newQty = (int) poItemModel.getValueAt(row, 1) + qty;
                // Unit cost may be corrected on re-add
                java.math.BigDecimal newTotal =
                        unitCost.multiply(java.math.BigDecimal.valueOf(newQty))
                                .setScale(2, java.math.RoundingMode.HALF_UP);
                
                // setValueAt(Object, int, int) � fix the calls:
                poItemModel.setValueAt(newQty,                            row, 1);
                poItemModel.setValueAt(String.format("%.2f", unitCost),   row, 2);
                poItemModel.setValueAt(String.format("%.2f", newTotal),   row, 3);
                updateTotal();
                resetItemInputs();
                return;
            }
        }        java.math.BigDecimal lineTotal =
                unitCost.multiply(java.math.BigDecimal.valueOf(qty))
                        .setScale(2, java.math.RoundingMode.HALF_UP);
        poItemModel.addRow(new Object[]{
                product.getProductCode() + "  " + product.getProductName(),
                qty,
                String.format("%.2f", unitCost),
                String.format("%.2f", lineTotal),
                product.getProductId()          // hidden column 4
        });

        updateTotal();
        resetItemInputs();
    }

    private void resetItemInputs() {
        cmbProduct.setSelectedIndex(0);
        txtQty.setText("1");
        txtCost.setText("0.00");
    }

    private void updateTotal() {
        java.math.BigDecimal grand = java.math.BigDecimal.ZERO;
        for (int r = 0; r < poItemModel.getRowCount(); r++) {
            grand = grand.add(new java.math.BigDecimal(
                    poItemModel.getValueAt(r, 3).toString()));
        }
        lblGrandTotal.setText(String.format("%.2f", grand));
    }
    // PLACE ORDER

    private void btnPlaceOrderActionPerformed() {        int supplierIdx = cmbSupplier.getSelectedIndex() - 1;
        if (supplierIdx < 0) {
            warn("Please select a supplier."); return;
        }
        if (poItemModel.getRowCount() == 0) {
            warn("Add at least one product before placing the order."); return;
        }

        com.mycompany.inventorycontrolsystem.model.Supplier supplier =
                supplierList.get(supplierIdx);        com.mycompany.inventorycontrolsystem.model.PurchaseOrder po =
                new com.mycompany.inventorycontrolsystem.model.PurchaseOrder();
        po.setSupplierId(supplier.getSupplierId());
        po.setUserId    (currentUser != null ? currentUser.getUserId() : 1);
        po.setPoNumber  (poDAO.generatePoNumber());
        po.setOrderDate (java.time.LocalDate.now());
        po.setStatus    (com.mycompany.inventorycontrolsystem.model
                         .PurchaseOrder.Status.PENDING);
        po.setNotes     ("");

        java.math.BigDecimal grand = java.math.BigDecimal.ZERO;
        for (int r = 0; r < poItemModel.getRowCount(); r++) {
            int    productId = (int) poItemModel.getValueAt(r, 4);
            int    qty       = (int) poItemModel.getValueAt(r, 1);
            java.math.BigDecimal cost = new java.math.BigDecimal(
                    poItemModel.getValueAt(r, 2).toString());

            com.mycompany.inventorycontrolsystem.model.PurchaseOrderItem item =
                    new com.mycompany.inventorycontrolsystem.model.PurchaseOrderItem();
            item.setProductId      (productId);
            item.setQuantityOrdered(qty);
            item.setUnitCost       (cost);
            po.addItem(item);
            grand = grand.add(cost.multiply(java.math.BigDecimal.valueOf(qty)));
        }
        po.setTotalAmount(grand.setScale(2, java.math.RoundingMode.HALF_UP));        btnPlaceOrder.setEnabled(false);
        final com.mycompany.inventorycontrolsystem.model.PurchaseOrder finalPo = po;

        new javax.swing.SwingWorker<Integer, Void>() {
            @Override protected Integer doInBackground() {
                // insertWithItems handles setAutoCommit/commit/rollback
                return poDAO.insertWithItems(finalPo);
            }
            @Override protected void done() {
                btnPlaceOrder.setEnabled(true);
                try {
                    int poId = get();
                    if (poId > 0) {
                        lastPlacedPoId = poId;
                        javax.swing.JOptionPane.showMessageDialog(
                                PurchaseOrderPanel.this,
                                "Purchase Order placed successfully!\n"
                                + "PO Number : " + finalPo.getPoNumber()
                                + "\nPO ID     : " + poId
                                + "\nTotal     : RM " + finalPo.getTotalAmount()
                                + "\n\nClick \"Receive Stock\" when goods arrive.",
                                "Order Placed",
                                javax.swing.JOptionPane.INFORMATION_MESSAGE);
                        clearForm();
                    } else {
                        warn("Failed to save the purchase order.\n"
                           + "Check the application log for the SQL error.");
                    }
                } catch (java.util.concurrent.ExecutionException ex) {
                    ex.printStackTrace();
                    warn("Unexpected error:\n" + ex.getCause().getMessage());
                } catch (InterruptedException ex) {
                    Thread.currentThread().interrupt();
                }
            }
        }.execute();
    }
    // RECEIVE STOCK

    private void btnReceiveStockActionPerformed() {
        int targetPoId = lastPlacedPoId;

        if (targetPoId <= 0) {
            String input = javax.swing.JOptionPane.showInputDialog(this,
                    "No order was placed in this session.\n"
                    + "Enter the PO ID to receive stock for:",
                    "Receive Stock",
                    javax.swing.JOptionPane.QUESTION_MESSAGE);
            if (input == null || input.trim().isEmpty()) return;
            try {
                targetPoId = Integer.parseInt(input.trim());
            } catch (NumberFormatException ex) {
                warn("PO ID must be a numeric value."); return;
            }
        }

        final int finalPoId = targetPoId;

        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
                "Mark PO ID " + finalPoId + " as RECEIVED?\n\n"
                + "This will add the ordered quantities into inventory stock.\n"
                + "This action cannot be undone.",
                "Confirm Receive Stock",
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.WARNING_MESSAGE);
        if (confirm != javax.swing.JOptionPane.YES_OPTION) return;

        btnReceiveStock.setEnabled(false);

        new javax.swing.SwingWorker<Boolean, Void>() {
            @Override protected Boolean doInBackground() {
                // receiveOrder handles the full transactional stock upsert:
                // INSERT INTO inventory_stock ... ON DUPLICATE KEY UPDATE
                // quantity_on_hand = quantity_on_hand + ordered_qty
                return poDAO.receiveOrder(finalPoId);
            }
            @Override protected void done() {
                btnReceiveStock.setEnabled(true);
                try {
                    boolean ok = get();
                    if (ok) {
                        lastPlacedPoId = -1;
                        System.out.println("PO ID={} received � stock updated.");
                        javax.swing.JOptionPane.showMessageDialog(
                                PurchaseOrderPanel.this,
                                "Stock received for PO ID: " + finalPoId
                                + "\nInventory has been updated.",
                                "Stock Received",
                                javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        warn("Receive failed for PO ID: " + finalPoId
                           + "\n\nPossible reasons:\n"
                           + "  - PO has no items\n"
                           + "  - PO already marked RECEIVED.");
                    }
                } catch (java.util.concurrent.ExecutionException ex) {
                    ex.printStackTrace();
                    warn("Unexpected error:\n" + ex.getCause().getMessage());
                } catch (InterruptedException ex) {
                    Thread.currentThread().interrupt();
                }
            }
        }.execute();
    }
