    /**
     * Loads products into the table on a background thread.
     * Passing {@code null} loads all products; passing a keyword performs search.
     *
     * @param keyword search term, or {@code null} to load all
     */
    private void loadProductsAsync(String keyword) {
        // Show a loading indicator in the table header area
        btnRefresh.setEnabled(false);
        btnSearch.setEnabled(false);

        new SwingWorker<List<Product>, Void>() {

            @Override
            protected List<Product> doInBackground() {
                return (keyword == null || keyword.isBlank())
                        ? controller.getAllProducts()
                        : controller.searchProducts(keyword);
            }

            @Override
            protected void done() {
                try {
                    List<Product> products = get();
                    liveTableModel.setRowCount(0);          // clear existing rows

                    for (Product p : products) {
                        liveTableModel.addRow(new Object[]{
                            p.getProductCode(),
                            p.getProductName(),
                            p.getCategoryName() != null ? p.getCategoryName() : "—",
                            String.format("%.2f", p.getSellingPrice()),
                            p.getQuantityOnHand(),
                            p.getProductId()                // hidden ID column
                        });
                    }

                    // Auto-resize columns after data is populated
                    autoResizeColumns(productTable);

                } catch (ExecutionException ex) {
                    ex.getCause().printStackTrace();
                    JOptionPane.showMessageDialog(
                            ProductFrame.this,
                            "Could not load products.\nCheck database connection.",
                            "Load Error", JOptionPane.ERROR_MESSAGE);
                } catch (InterruptedException ex) {
                    Thread.currentThread().interrupt();
                } finally {
                    btnRefresh.setEnabled(true);
                    btnSearch.setEnabled(true);
                }
            }
        }.execute();
    }
