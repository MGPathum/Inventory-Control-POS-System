    /** Triggered when Search button is clicked (or Enter pressed in txtSearch). */
    private void onSearchClicked() {
        String keyword = txtSearch.getText().trim();
        // Ignore if the field still contains the design-time placeholder text
        if (keyword.isEmpty()
                || keyword.equalsIgnoreCase("Search :")
                || keyword.equalsIgnoreCase("Search")) {
            loadProductsAsync(null);
        } else {
            loadProductsAsync(keyword);
        }
    }

    /**
     * Triggered when a row is selected in productTable.
     * Reads the hidden ID column and loads that product's full record
     * into all form fields.
     */
    private void onRowSelected() {
        int row = productTable.getSelectedRow();
        if (row < 0) return;

        int productId = (int) liveTableModel.getValueAt(row, 5);

        controller.getById(productId).ifPresent(p -> {
            editingProduct = p;

            txtCode.setText(p.getProductCode());
            txtName.setText(p.getProductName());
            txtCostPrice.setText(
                    p.getCostPrice() != null ? p.getCostPrice().toPlainString() : "0.00");
            txtSellingPrice.setText(
                    p.getSellingPrice() != null ? p.getSellingPrice().toPlainString() : "0.00");
            txtReorderLevel.setText(String.valueOf(p.getReorderLevel()));

            // Match category combo to this product's category
            cmbCategory.setSelectedIndex(0);    // default = "-- Select Category --"
            if (categoryList != null) {
                for (int i = 0; i < categoryList.size(); i++) {
                    if (categoryList.get(i).getCategoryId() == p.getCategoryId()) {
                        cmbCategory.setSelectedIndex(i + 1); // +1 for the placeholder item
                        break;
                    }
                }
            }

            // Status radio buttons
            if (p.isActive()) {
                radioActive.setSelected(true);
            } else {
                radioInactive.setSelected(true);
            }

            // Live barcode preview
            String barcode = p.getBarcode() != null ? p.getBarcode() : p.getProductCode();
            if (!barcode.isBlank()) {
                controller.renderBarcodeOnLabel(lblBarcodePreview, barcode);
            }

            System.out.println("Row selected → loaded product ID={}");
        });
    }

    /** Triggered by the Save button. Validates fields, then inserts or updates. */
    private void onSaveClicked() {
        String code = txtCode.getText().trim();
        String name = txtName.getText().trim();

        if (code.isEmpty()) {
            showError("Product Code is required.");
            txtCode.requestFocusInWindow();
            return;
        }
        if (name.isEmpty()) {
            showError("Product Name is required.");
            txtName.requestFocusInWindow();
            return;
        }
        if (cmbCategory.getSelectedIndex() == 0) {
            showError("Please select a Category.");
            cmbCategory.requestFocusInWindow();
            return;
        }

        BigDecimal costPrice, sellingPrice;
        int reorderLevel;
        try {
            costPrice    = new BigDecimal(txtCostPrice.getText().trim());
            sellingPrice = new BigDecimal(txtSellingPrice.getText().trim());
            reorderLevel = Integer.parseInt(
                    txtReorderLevel.getText().trim().isEmpty()
                    ? "10" : txtReorderLevel.getText().trim());
        } catch (NumberFormatException ex) {
            showError("Cost Price, Selling Price, and Reorder Level must be valid numbers.");
            return;
        }
        Product p = (editingProduct != null) ? editingProduct : new Product();
        p.setProductCode  (code);
        p.setProductName  (name);
        p.setCostPrice    (costPrice);
        p.setSellingPrice (sellingPrice);
        p.setTaxRate      (java.math.BigDecimal.ZERO);   // default 0% — no tax field in this form
        p.setReorderLevel (reorderLevel);
        p.setBarcode      (code);                        // use product code as barcode
        p.setDescription  ("");                          // empty string — never null
        p.setImagePath    (null);                        // nullable column — safe
        p.setActive       (radioActive.isSelected());
        p.setUnit         ("pcs");                       // default unit

        // Resolve category name → category_id
        int catIndex = cmbCategory.getSelectedIndex() - 1; // -1 for placeholder
        if (catIndex >= 0 && categoryList != null && catIndex < categoryList.size()) {
            p.setCategoryId(categoryList.get(catIndex).getCategoryId());
        }
        try {
            boolean success;
            if (editingProduct == null) {
                success = controller.addProduct(p) > 0;
            } else {
                success = controller.updateProduct(p);
            }

            if (success) {
                JOptionPane.showMessageDialog(this,
                        "Product saved successfully.",
                        "Success", JOptionPane.INFORMATION_MESSAGE);
                clearForm();
                loadProductsAsync(null);

                // Refresh the product dropdown in Purchase Order panel immediately
                if (purchaseOrderPanel != null) {
                    purchaseOrderPanel.refreshProductCombo();
                }
            } else {
                showError("Save failed.\n\n"
                        + "Possible causes:\n"
                        + "  • Product code already exists in the database\n"
                        + "  • A required field is missing or invalid\n\n"
                        + "Check the NetBeans Output window for the full SQL error.");
            }
        } catch (IllegalArgumentException ex) {
            showError(ex.getMessage());
        } catch (Exception ex) {
            // Catch-all so any unexpected runtime exception shows the real message
            ex.printStackTrace(); // prints full stack trace to NetBeans Output tab
            showError("Unexpected error during save:\n" + ex.getMessage());
        }
    }

    /** Triggered by the Deactivate button — soft-deletes the selected product. */
    private void onDeactivateClicked() {
        int row = productTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this,
                    "Please select a product from the table first.",
                    "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String productName = liveTableModel.getValueAt(row, 1).toString();
        int productId      = (int) liveTableModel.getValueAt(row, 5);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Deactivate product:\n\"" + productName + "\"?\n\n"
                + "The product will be hidden but its history is preserved.",
                "Confirm Deactivate",
                JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            boolean ok = controller.deactivateProduct(productId);
            if (ok) {
                clearForm();
                loadProductsAsync(null);
            } else {
            }
        }
    }

    /** Exports the product list to a real PDF file using Apache PDFBox. */
    private void onExportPDF() {
        JFileChooser fc = new JFileChooser();
        fc.setDialogTitle("Save PDF Report");
        fc.setSelectedFile(new java.io.File("Products_Report.pdf"));
        fc.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter(
                "PDF Files (*.pdf)", "pdf"));
        if (fc.showSaveDialog(this) != JFileChooser.APPROVE_OPTION) return;

        String path = fc.getSelectedFile().getAbsolutePath();
        if (!path.toLowerCase().endsWith(".pdf")) path += ".pdf";

        // Snapshot the visible columns (skip hidden ID column 5) on the EDT
        final int rowCount = liveTableModel.getRowCount();
        final java.util.List<Object[]> snapshot = new java.util.ArrayList<>(rowCount);
        for (int r = 0; r < rowCount; r++) {
            snapshot.add(new Object[]{
                liveTableModel.getValueAt(r, 0),  // Code
                liveTableModel.getValueAt(r, 1),  // Name
                liveTableModel.getValueAt(r, 2),  // Category
                liveTableModel.getValueAt(r, 3),  // Price (RM)
                liveTableModel.getValueAt(r, 4)   // Stock
            });
        }
        final String generatedBy = currentUser.getFullName();
        final String finalPath   = path;

        new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground() throws Exception {
                String[] headers = {"Product Code", "Product Name",
                                    "Category", "Price (RM)", "Stock Qty"};
                com.mycompany.inventorycontrolsystem.util.PdfExportUtil.exportTable(
                        "Product Catalogue Report",
                        headers,
                        snapshot,
                        finalPath,
                        generatedBy);
                return null;
            }

            @Override
            protected void done() {
                try {
                    get();
                    JOptionPane.showMessageDialog(ProductFrame.this,
                        "PDF exported successfully!\nSaved to:\n" + finalPath,
                        "Export Complete", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(ProductFrame.this,
                        "PDF export failed:\n" + ex.getMessage(),
                        "Export Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }.execute();
    }

    /** Exports the product list directly to Excel (.xlsx) using Apache POI. */
    private void onExportExcel() {
        JFileChooser fc = new JFileChooser();
        fc.setDialogTitle("Save Excel Report");
        fc.setSelectedFile(new java.io.File("Products_Report.xlsx"));
        fc.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter(
                "Excel Files (*.xlsx)", "xlsx"));
        if (fc.showSaveDialog(this) != JFileChooser.APPROVE_OPTION) return;

        String path = fc.getSelectedFile().getAbsolutePath();
        if (!path.toLowerCase().endsWith(".xlsx")) path += ".xlsx";

        // Snapshot the table data on the EDT before handing off to the worker
        final int rowCount = liveTableModel.getRowCount();
        final Object[][] snapshot = new Object[rowCount][5];
        for (int r = 0; r < rowCount; r++) {
            for (int c = 0; c < 5; c++) {
                snapshot[r][c] = liveTableModel.getValueAt(r, c);
            }
        }
        final String generatedBy = currentUser.getFullName();
        final String finalPath   = path;

        new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground() throws Exception {
                try (XSSFWorkbook workbook = new XSSFWorkbook()) {

                    Sheet sheet = workbook.createSheet("Products");
                    CellStyle headerStyle = workbook.createCellStyle();
                    org.apache.poi.ss.usermodel.Font headerFont = workbook.createFont();
                    headerFont.setBold(true);
                    headerFont.setColor(IndexedColors.WHITE.getIndex());
                    headerFont.setFontHeightInPoints((short) 11);
                    headerStyle.setFont(headerFont);
                    headerStyle.setFillForegroundColor(
                        new XSSFColor(new byte[]{(byte) 34, (byte) 45, (byte) 65}, null));
                    headerStyle.setFillPattern(FillPatternType.SOLID_FOREGROUND);
                    headerStyle.setBorderBottom(BorderStyle.MEDIUM);
                    headerStyle.setAlignment(HorizontalAlignment.LEFT);
                    CellStyle altStyle = workbook.createCellStyle();
                    altStyle.setFillForegroundColor(
                        new XSSFColor(new byte[]{(byte) 245, (byte) 247, (byte) 250}, null));
                    altStyle.setFillPattern(FillPatternType.SOLID_FOREGROUND);
                    Row titleRow = sheet.createRow(0);
                    titleRow.setHeightInPoints(22);
                    Cell titleCell = titleRow.createCell(0);
                    titleCell.setCellValue("INVENTORY CONTROL SYSTEM — Product Catalogue Report");
                    CellStyle titleStyle = workbook.createCellStyle();
                    org.apache.poi.ss.usermodel.Font titleFont = workbook.createFont();
                    titleFont.setBold(true);
                    titleFont.setFontHeightInPoints((short) 14);
                    titleFont.setColor(IndexedColors.DARK_BLUE.getIndex());
                    titleStyle.setFont(titleFont);
                    titleCell.setCellStyle(titleStyle);
                    sheet.addMergedRegion(new CellRangeAddress(0, 0, 0, 4));
                    Row subRow = sheet.createRow(1);
                    subRow.createCell(0).setCellValue(
                        "Generated by: " + generatedBy + "   |   " + new java.util.Date());
                    sheet.addMergedRegion(new CellRangeAddress(1, 1, 0, 4));

                    sheet.createRow(2); // blank spacer
                    String[] headers = {
                        "Product Code", "Product Name",
                        "Category", "Price (RM)", "Stock Qty"
                    };
                    Row hdrRow = sheet.createRow(3);
                    hdrRow.setHeightInPoints(18);
                    for (int i = 0; i < headers.length; i++) {
                        Cell c = hdrRow.createCell(i);
                        c.setCellValue(headers[i]);
                        c.setCellStyle(headerStyle);
                    }
                    for (int r = 0; r < snapshot.length; r++) {
                        Row dataRow = sheet.createRow(r + 4);
                        for (int c = 0; c < snapshot[r].length; c++) {
                            Cell cell = dataRow.createCell(c);
                            Object val = snapshot[r][c];
                            if (val instanceof Number) {
                                cell.setCellValue(((Number) val).doubleValue());
                            } else {
                                cell.setCellValue(val != null ? val.toString() : "");
                            }
                            if (r % 2 == 1) cell.setCellStyle(altStyle);
                        }
                    }
                    for (int i = 0; i < headers.length; i++) {
                        sheet.autoSizeColumn(i);
                        // Add a little extra padding beyond autoSizeColumn
                        sheet.setColumnWidth(i, sheet.getColumnWidth(i) + 1024);
                    }
                    try (FileOutputStream fos = new FileOutputStream(finalPath)) {
                        workbook.write(fos);
                    }
                }
                return null;
            }

            @Override
            protected void done() {
                try {
                    get();
                    JOptionPane.showMessageDialog(ProductFrame.this,
                        "Export Successful!\n\nReport saved to:\n" + finalPath,
                        "Export Complete", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(ProductFrame.this,
                        "Export failed:\n" + ex.getMessage(),
                        "Export Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }.execute();
    }
