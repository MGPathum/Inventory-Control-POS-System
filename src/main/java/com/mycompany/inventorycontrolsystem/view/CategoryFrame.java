    /** Returns a human-readable role name from a numeric role ID. */
    private String getRoleName(int roleId) {
        return switch (roleId) {
            case 1  -> "Admin";
            case 2  -> "Manager";
            default -> "Cashier";
        };
    }

    /**
     * Resizes every column to fit both its header text and the widest
     * cell value currently in the table.
     * Called from SwingWorker.done() so row data is present when measured.
     *
     * @param table the JTable to auto-size (null-safe)
     */
    private void autoResizeColumns(javax.swing.JTable table) {
        if (table == null) return;
        final javax.swing.table.TableColumnModel columnModel = table.getColumnModel();
        for (int column = 0; column < table.getColumnCount(); column++) {
            int width = 50; // minimum

            // Measure header
            javax.swing.table.TableCellRenderer headerRenderer =
                table.getTableHeader().getDefaultRenderer();
            if (headerRenderer != null) {
                java.awt.Component comp = headerRenderer
                    .getTableCellRendererComponent(table,
                        columnModel.getColumn(column).getHeaderValue(),
                        false, false, 0, column);
                width = Math.max(comp.getPreferredSize().width + 25, width);
            }

            // Measure widest data cell
            for (int row = 0; row < table.getRowCount(); row++) {
                javax.swing.table.TableCellRenderer renderer =
                    table.getCellRenderer(row, column);
                java.awt.Component comp =
                    table.prepareRenderer(renderer, row, column);
                width = Math.max(comp.getPreferredSize().width + 15, width);
            }

            columnModel.getColumn(column).setPreferredWidth(width);
        }
    }

