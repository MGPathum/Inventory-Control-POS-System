    /**
     * Reloads only the product combo box from the database.
     * Called by ProductFrame immediately after a new product is saved,
     * so the Purchase Order screen always has an up-to-date product list.
     */
    public void refreshProductCombo() {
        new javax.swing.SwingWorker<java.util.List<com.mycompany.inventorycontrolsystem.model.Product>, Void>() {
            @Override
            protected java.util.List<com.mycompany.inventorycontrolsystem.model.Product> doInBackground() {
                return productDAO.findAll();
            }
            @Override
            protected void done() {
                try {
                    java.util.List<com.mycompany.inventorycontrolsystem.model.Product> products = get();
                    productList.clear();
                    cmbProduct.removeAllItems();
                    cmbProduct.addItem("-- Select Product --");
                    for (com.mycompany.inventorycontrolsystem.model.Product p : products) {
                        productList.add(p);
                        cmbProduct.addItem(p.getProductCode() + "  " + p.getProductName());
                    }
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            }
        }.execute();
    }

    private void loadComboBoxes() {
        new javax.swing.SwingWorker<Void, Void>() {
            java.util.List<com.mycompany.inventorycontrolsystem.model.Supplier> suppliers;
            java.util.List<com.mycompany.inventorycontrolsystem.model.Product>  products;

            @Override protected Void doInBackground() {
                suppliers = supplierDAO.findActive();
                products  = productDAO.findAll();
                return null;
            }

            @Override protected void done() {
                try { get(); } catch (Exception ex) {
                    ex.printStackTrace();
                    javax.swing.JOptionPane.showMessageDialog(
                            PurchaseOrderPanel.this,
                            "Could not load suppliers / products.\n"
                            + "Check database connection.",
                            "Load Error",
                            javax.swing.JOptionPane.ERROR_MESSAGE);
                    return;
                }

                // Populate cmbSupplier
                supplierList.clear();
                cmbSupplier.removeAllItems();
                cmbSupplier.addItem("-- Select Supplier --");
                for (com.mycompany.inventorycontrolsystem.model.Supplier s
                        : suppliers) {
                    supplierList.add(s);
                    cmbSupplier.addItem(s.getCompanyName());
                }

                // Populate cmbProduct
                productList.clear();
                cmbProduct.removeAllItems();
                cmbProduct.addItem("-- Select Product --");
                for (com.mycompany.inventorycontrolsystem.model.Product p
                        : products) {
                    productList.add(p);
                    cmbProduct.addItem(p.getProductCode()
                            + "  " + p.getProductName());
                }

            }
        }.execute();
    }
