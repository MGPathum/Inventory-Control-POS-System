    // DATA LOADING — SwingWorker

    /**
     * Fetches all categories from the database on a background thread,
     * then populates {@code pnlTable} on the EDT.
     * Called on startup and after every save / delete operation.
     */
    private void loadCategoriesAsync() {
        new SwingWorker<List<Category>, Void>() {

            @Override
            protected List<Category> doInBackground() {
                return DAOFactory.getCategoryDAO().findAll();
            }

            @Override
            protected void done() {
                try {
                    List<Category> categories = get();
                    liveTableModel.setRowCount(0);      // clear existing rows

                    for (Category c : categories) {
                        liveTableModel.addRow(new Object[]{
                            c.getCategoryId(),
                            c.getCategoryName()
                        });
                    }

                    // Auto-resize columns after data is populated
                    autoResizeColumns(pnlTable);

                } catch (ExecutionException ex) {
                    ex.getCause().printStackTrace();
                    JOptionPane.showMessageDialog(
                            CategoryFrame.this,
                            "Could not load categories.\nCheck database connection.",
                            "Load Error", JOptionPane.ERROR_MESSAGE);
                } catch (InterruptedException ex) {
                    Thread.currentThread().interrupt();
                }
            }
        }.execute();
    }
