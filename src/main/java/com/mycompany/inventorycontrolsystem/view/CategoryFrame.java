    /**
     * Row click handler — reads the selected row and populates the form fields.
     * txtId shows the category_id (read-only).
     * txtName shows the category name (editable for updates).
     */
    private void onRowSelected() {
        int row = pnlTable.getSelectedRow();
        if (row < 0) return;

        int    id   = (int)    liveTableModel.getValueAt(row, 0);
        String name = (String) liveTableModel.getValueAt(row, 1);

        // Load the full Category object so we have all fields for the update
        DAOFactory.getCategoryDAO().findById(id).ifPresent(c -> {
            editingCategory = c;
            txtId  .setText(String.valueOf(c.getCategoryId()));
            txtName.setText(c.getCategoryName());
            System.out.println("Row selected → category ID={}");
        });
    }

    /**
     * Save button handler.
     * Inserts a new category when {@code editingCategory} is null,
     * or updates the existing one when a row was previously selected.
     *
     * Validation rules:
     *   - Category Name must not be blank.
     */
    private void onSaveClicked() {
        String name = txtName.getText().trim();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Category Name cannot be empty.",
                    "Validation Error", JOptionPane.WARNING_MESSAGE);
            txtName.requestFocusInWindow();
            return;
        }
        boolean isNew = (editingCategory == null);
        Category c = isNew ? new Category() : editingCategory;
        c.setCategoryName(name);
        c.setActive(true);
        boolean success;
        if (isNew) {
            success = DAOFactory.getCategoryDAO().insert(c) > 0;
        } else {
            success = DAOFactory.getCategoryDAO().update(c);
        }

        if (success) {
            String action = isNew ? "added" : "updated";
            JOptionPane.showMessageDialog(this,
                    "Category '" + name + "' saved successfully.",
                    "Success", JOptionPane.INFORMATION_MESSAGE);
            clearForm();
            loadCategoriesAsync();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Save failed. The category name may already exist.\n"
                    + "Check the application log for details.",
                    "Save Failed", JOptionPane.ERROR_MESSAGE);
        }
    }

    /**
     * Delete button handler.
     * Requires a row to be selected first.
     * Shows a confirmation dialog before calling the DAO.
     *
     * Note: deletion will fail if products are linked to this category
     * (enforced by the FK constraint in MySQL).
     */
    private void onDeleteClicked() {
        int row = pnlTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this,
                    "Please select a category from the table first.",
                    "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int    id   = (int)    liveTableModel.getValueAt(row, 0);
        String name = (String) liveTableModel.getValueAt(row, 1);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete category:  \"" + name + "\"?\n\n"
                + "WARNING: This will fail if products are assigned to this category.",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            boolean ok = DAOFactory.getCategoryDAO().delete(id);
            if (ok) {
                clearForm();
                loadCategoriesAsync();
            } else {
                JOptionPane.showMessageDialog(this,
                        "Delete failed.\n"
                        + "This category may have products assigned to it.\n"
                        + "Reassign or deactivate those products first.",
                        "Delete Failed", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
