    /**
     * Fetches the next PROD-XXXX code from the database on a background thread
     * and applies it to txtCode on the EDT.
     *
     * Called on startup (from setupData) and whenever clearForm() resets
     * the form for a new product entry.  txtCode is locked non-editable so
     * the generated value is the only way a code gets into that field for
     * new records.
     */
    private void loadNextProductCode() {
        // Disable the field while loading so the user can't type in a stale state
        txtCode.setEditable(false);
        txtCode.setEnabled(false);
        txtCode.setText("Generating...");

        new SwingWorker<String, Void>() {
            @Override
            protected String doInBackground() {
                return controller.generateNextProductCode();
            }

            @Override
            protected void done() {
                try {
                    String nextCode = get();
                    txtCode.setText(nextCode);
                    // Style it visually so the admin knows it is auto-generated
                    txtCode.setBackground(new java.awt.Color(235, 245, 255));
                    txtCode.setForeground(new java.awt.Color(20, 30, 80));
                    txtCode.setFont(new java.awt.Font("JetBrains Mono", java.awt.Font.BOLD, 14));
                    txtCode.setToolTipText("Auto-generated — format is locked to PROD-XXXX");
                    System.out.println("Next product code generated: {}");
                } catch (ExecutionException ex) {
                    ex.getCause().printStackTrace();
                    txtCode.setText("PROD-0001");   // safe fallback
                } catch (InterruptedException ex) {
                    Thread.currentThread().interrupt();
                    txtCode.setText("PROD-0001");
                } finally {
                    // Keep non-editable: admin must never free-type a product code
                    txtCode.setEditable(false);
                    txtCode.setEnabled(true);
                }
            }
        }.execute();
    }

